package Wj;

public abstract class Operator {
    private int left;
    private int right;
    public abstract int cal();

    public int getLeft() {
        return left;
    }

    public void setLeft(int left) {
        this.left = left;
    }

    public int getRight() {
        return right;
    }

    public void setRight(int right) {
        this.right = right;
    }
}
